#include <stdio.h>
int main(){
    char ch;
    printf("");
    scanf("%c",&ch);
    if (ch==a,e,i,o,u) {
        printf("Vowel");}
    else if(!(ch==a,i,o,u,e)) {
        printf("Consonant");}
    else if(ch==1,2,3,4,5,6,7,8,9) {
        printf("Digit");}
    else { printf("Special Character");}
    return 0;
}